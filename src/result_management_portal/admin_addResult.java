    private void save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_btnActionPerformed
        // TODO add your handling code here:
        String roll_no = roll_no_txt.getText();
        String physics_marks = physics_marks_txt.getText();
        String math_marks = math_marks_txt.getText();
        String it_marks = it_marks_txt.getText();
        String dbms_marks = dbms_marks_txt.getText();
        String os_marks = os_marks_txt.getText();

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresultmansystem","root","12345");
            Statement st = con.createStatement();
            // check if roll no present in student table or not?
            String qry = "select * from student where rollno='"+roll_no+"'";
            ResultSet rs = st.executeQuery(qry);
            
            if(rs.next())
            {
                st.executeUpdate("insert into result(roll_no,physics,math,it,dbms,os) values('"+roll_no+"','"+physics_marks+"','"+math_marks+"','"+it_marks+"','"+dbms_marks+"','"+os_marks+"')");
                JOptionPane.showMessageDialog(null, "Result Added!");
                setVisible(false);
                new admin_addResult().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Student not present in system! check roll No.");
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }//GEN-LAST:event_save_btnActionPerformed

