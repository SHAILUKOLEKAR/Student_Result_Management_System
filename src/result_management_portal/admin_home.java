    private void save_new_student_data_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_new_student_data_btnActionPerformed
        // TODO add your handling code here:
        String course = (String) course_combo.getSelectedItem();
        String branch_name = (String)branch_combo.getSelectedItem();
        String roll_no = roll_no_txt.getText();
        String student_name = student_name_txt.getText();
        String gender = (String)gender_combo.getSelectedItem();
        String father_name = father_name_txt.getText();

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresultmansystem","root","12345");
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollno,course,branchName,name,gender,fatherName) values('"+roll_no+"','"+course+"','"+branch_name+"','"+student_name+"','"+gender+"','"+father_name+"')");
            JOptionPane.showMessageDialog(null, "Data saved!");
            setVisible(false);
            new admin_home().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_save_new_student_data_btnActionPerformed

