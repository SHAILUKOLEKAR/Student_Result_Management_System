    private void search_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_btnActionPerformed
        // TODO add your handling code here:
        String roll_no = student_roll_no_txt.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresultmansystem","root","12345");
            Statement st = con.createStatement();
            String qry = "select * from result where roll_no = '"+roll_no+"'";
            ResultSet rs = st.executeQuery(qry);
            if(rs.next())
            {
                setVisible(false);
                new student_index(roll_no).setVisible(true);
            }
            else
            {
                 JOptionPane.showMessageDialog(null, "Your roll no is not present in system!");
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_search_btnActionPerformed

